    // VALIDATES INPUT FOR CALCULATION, ID AND MONTH ARE BOTH REQUIRED
    private Integer inputValidation(String empID, String monthText) {
    // Check if Employee ID is missing
    if (empID == null || empID.trim().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Employee ID is required", "Missing input", JOptionPane.ERROR_MESSAGE);
        return null;
    }

    // Check if Employee ID is numeric
    try {
        Integer.parseInt(empID);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Employee ID must be numeric", "Invalid input", JOptionPane.ERROR_MESSAGE);
        return null;
    }

    // Validate Month
    try {
        int month = Integer.parseInt(monthText);
        if (month < 1 || month > 12) {
            JOptionPane.showMessageDialog(null, "Months must be 1-12", "Invalid input", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        return month;
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Months must be numeric", "Invalid input", JOptionPane.ERROR_MESSAGE);
        return null;
    }
}
   
